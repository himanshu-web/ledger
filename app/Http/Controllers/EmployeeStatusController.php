    public function update($id)
    {
        $status = User::select('id','status')->where('id',$id)->get();
        
// dd($status[0]->status);
        if($status[0]->status == 1){
            $st = User::find($id);
            $st->status = 0;
            $st->save();
        }
        if($status[0]->status == 0){
            $st = User::find($id);
            $st->status = 1;
            $st->save();
        }

        return back()->with('success','Status Update Successfull');
    }

